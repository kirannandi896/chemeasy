function get_electronic_config() {
  var atomic_no = document.getElementById("element").value;
  for (var i = 0; i < elements.length; i++) {
    if (elements[i].values.atomic_number == atomic_no) {
      document.getElementById("electronic_config").innerHTML = elements[
        i
      ].values.electronic_configuration.replace(/\(|\)/gi, "  ");
      break;
    }
  } 
}
function cal_shielding_constant(econfig, n, l) {
  var elc = econfig.trim();
  elc.replace("  ", "");
  var subshells = elc.split("    ");
  // console.log(subshells);
  var shell = "";
  if (l == 0){
    shell = "s";
  }
  else if (l==1){
    shell = "p";
  }
  else if (l==2){
    shell = "d";
  }
  else if (l==3){
    shell = "f";
  }
  var shell_l = n.toString();
  var subshel = shell_l+shell;
  console.log(subshel);
  var newsubshells = subshells.map((str) => str.slice(0, -1));
  // console.log(newsubshells)
  for (var q of newsubshells){
    if (q.length == 3){
      newsubshells[newsubshells.indexOf(q)] = q.slice(0, -1);
    }
  }
  newsubshells.sort((a, b) => a[0] - b[0]);
  // console.log(newsubshells)
  if (newsubshells.includes(subshel)){
    let sheilding = 0;
    
    for (const subshell of subshells.sort((a, b) => a[0] - b[0])) {
      if (n == 1 && l == 0) {
        sheilding += (parseInt(subshell[2]) - 1) * 0.3;
        break;
      }
      // if (subshell.slice(0,2) == subshel){
      //   sheilding += (parseInt(subshell[2]) - 1) * 0.35;
      //   break;
      // }
      else if (parseInt(subshell[0]) <= n) { 
        if (l==0 || l==1) {
          if (subshell.length == 3) {
            if (parseInt(subshell[0]) == n) {
              if (
                (subshell[1] == "s" && l == 0) ||
                (subshell[1] == "p" && l == 1) ||
                (subshell[1] == "d" && l == 2) ||
                (subshell[1] == "f" && l == 3)
              ) {
                sheilding += (parseInt(subshell[2]) - 1) * 0.35;
              } 
              else {
                sheilding += parseInt(subshell[2]) * 0.35;
              }
            } else if (parseInt(subshell[0]) == n - 1) {
              sheilding += parseInt(subshell[2]) * 0.85;
            } else {
              sheilding += parseInt(subshell[2]);
            }
          } 

          else if (subshell.length == 4) {
            if (parseInt(subshell[0]) == n) {
              if (
                (subshell[1] == "s" && l == 0) ||
                (subshell[1] == "p" && l == 1) ||
                (subshell[1] == "d" && l == 2) ||
                (subshell[1] == "f" && l == 3)
              ) {
                sheilding += (parseInt(subshell.slice(2, 4)) - 1) * 0.35;
              } else {
                sheilding += parseInt(subshell.slice(2, 4)) * 0.35;
              }
            } 

            else if (parseInt(subshell[0]) == n - 1) {
              sheilding += parseInt(subshell.slice(2, 4)) * 0.85;
            } 

            else {
              sheilding += parseInt(subshell.slice(2, 4));
            }
          }
        } 
        
        else if (l==2 || l==3) {
          if (subshell.length == 3) {
            if (parseInt(subshell[0]) == n) {
              if (
                (subshell[1] == "s" && l == 0) ||
                (subshell[1] == "p" && l == 1) ||
                (subshell[1] == "d" && l == 2) ||
                (subshell[1] == "f" && l == 3)
              ) {
                sheilding += (parseInt(subshell[2]) - 1) * 0.35;
              } 
              else {
                sheilding += parseInt(subshell[2]) * 0.35;
              }
            } else {
              sheilding += parseInt(subshell[2]);
            }
          } 

          else if (subshell.length == 4) {
            if (parseInt(subshell[0]) == n) {
              if (
                (subshell[1] == "s" && l == 0) ||
                (subshell[1] == "p" && l == 1) ||
                (subshell[1] == "d" && l == 2) ||
                (subshell[1] == "f" && l == 3)
              ) {
                sheilding += (parseInt(subshell.slice(2, 4)) - 1) * 0.35;
              } else {
                sheilding += parseInt(subshell.slice(2, 4)) * 0.35;
              }
            } 
            else {
              sheilding += parseInt(subshell.slice(2, 4));
            }
          }
        }
      }

      // console.log(sheilding)
    }
    return sheilding.toFixed(2);
  }

  else{
    return "err";
  }
  
}

function calculateEffectiveNuclearCharge() {
  get_electronic_config();
  const elec_config = document.getElementById("electronic_config").textContent;
  const n = document.getElementById("n").value;
  const l = document.getElementById("l").value;
  var ls = "s";
  if (l == 0) {
    ls = "s";
  } else if (l == 1) {
    ls = "p";
  } else if (l == 2) {
    ls = "d";
  } else if (l == 3) {
    ls = "f";
  }

  document.getElementById("op_orbital").textContent = n + ls;
  const shielding = cal_shielding_constant(elec_config, n, l);
  if (shielding == "err") {
    document.getElementById("op_shielding").textContent = "Error";
    document.getElementById("op_effective_charge").textContent = "You have selected wrong orbitals.";
    return;
  }
  const atomicno = document.getElementById("element").value;
  const effective_nuclear_charge = (atomicno - shielding).toFixed(2);
  document.getElementById("op_shielding").textContent = shielding;
  document.getElementById("op_effective_charge").textContent = effective_nuclear_charge;
}

